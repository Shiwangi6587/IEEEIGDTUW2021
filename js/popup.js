// Show the pop-up
function showPopup() {
     document.getElementById('popupContainer').style.display = 'block';
 }
 
 // Close the pop-up
 function closePopup() {
     document.getElementById('popupContainer').style.display = 'none';
 }
 
 // Trigger the pop-up (you can call this function wherever you want)
 showPopup();
 